    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        
        // instancia a classe Usuario model
        Cliente obj = new Cliente();
// executa o mÃ©todo setcpf para preencher o cpf
        obj.setCpf(this.textCPF.getText());
// instancia o objeto Controller Clientedao
        ClienteDao u1 = new ClienteDao();
// executa o mÃ©todo buscar e coloca os registros dentro
// do ResultSet resul
        ResultSet resul = u1.buscar(obj);
        try {
            if (resul.next()) {
// se o registro foi encontrado preenche os campos do formulÃ¡rio usuÃ¡rios
                this.textNome.setText(resul.getString("nome"));
                this.textEmail.setText(resul.getString("email"));
                this.textCelular.setText(resul.getString("celular"));
                this.textEndereco.setText(resul.getString("endereco"));
            } else {
                JOptionPane.showMessageDialog(null, "Registro nÃ£o encontrado!");
                this.textCPF.grabFocus();
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_btnBuscarMouseClicked

