        // TODO add your handling code here:
        DefaultTableModel MdlTableCli =  (DefaultTableModel)
        this.tblJogos.getModel();
        Object[] linhas = {this.textNome.getText(), this.textTg.getText(),
            this.textProdutora.getText(), this.textPlataforma.getText(), this.textIdadeInd.getText()};
