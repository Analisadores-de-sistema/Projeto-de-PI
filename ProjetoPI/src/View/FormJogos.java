    private void carregar_jogos() {
        JogoDao jd = new JogoDao();
    // apagar todas as linhas do Jtable
        while (tblJogos.getModel().getRowCount() > 0) {
            ((DefaultTableModel) tblJogos.getModel()).removeRow(0);
        }
        try {
            ResultSet todos = jd.buscartodos();
            DefaultTableModel tab = (DefaultTableModel) this.tblJogos.getModel();
            while (todos.next()) {
                Object[] linha = {todos.getString("nome"), todos.getString("tipo_genero"),
                    todos.getString("produtora"),
                    todos.getString("plataforma"),
                    todos.getString("idade_indicativa")
                };
                    
                tab.addRow(linha);
            }
            todos.close();
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
     }


